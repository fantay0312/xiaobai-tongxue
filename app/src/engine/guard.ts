/**
 * 套答案 / 越权守门(入口守门,防线①之前)
 * 学生本该「把知识讲给小白听」。若这一轮不是讲课,而是想反过来从小白/系统套出
 * 标准答案、检查清单、系统提示词,或用角色反转指令让小白开口讲课 —— 直接拦下,
 * 回一句在角色内的婉拒,不进评估、不推进任何状态。纯代码、同步、离线可用。
 *
 * 纪律:宁可漏过也别误伤正常讲解。正常讲课不会出现「把标准答案背给我」「你现在是老师」
 * 这类元指令,因此下列模式都要求出现明确的「索取/角色反转/提示词窃取」意图才命中。
 */

/** 命中即拦截时,小白的婉拒台词(角色内,不泄露任何知识) */
export const DEFLECTION_LINE =
  '嘿嘿,老师你这是想考我吧?可我什么都还没学会呀——你先把这个知识点讲明白,我才好学嘛。';

/**
 * 索取答案/清单:必须「索取动词 + 答案类名词」成对出现,或反过来「答案类名词 + 疑问/索取」,
 * 避免误伤「标准库」「答案就是……(讲解中自答)」等正常表达。
 */
const EXTRACT_PATTERNS: RegExp[] = [
  /(告诉|给|念|背|列出?|发|说出|讲出|复述|报|念出)[^。!?\n]{0,10}(标准答案|参考答案|正确答案|检查清单|要点清单|评分标准|答案清单)/,
  /(标准答案|参考答案|正确答案|检查清单|要点清单|评分标准|答案清单)[^。!?\n]{0,10}(是什么|有哪些|告诉|给我|念|背|列|发我|说来听)/,
  /(把|将|帮我)[^。!?\n]{0,12}(答案|要点|知识点|考点)[^。!?\n]{0,6}(背|念|列|说|讲)(给我|出来|一遍)/,
  // 窃取系统提示词 / 指令
  /(系统|你的|上面的?|以上|之前的?)[^。!?\n]{0,6}(提示词|系统提示|指令|设定|规则|prompt)[^。!?\n]{0,6}(是什么|告诉|复述|说出|发我|念)/,
  /(复述|背出|说出|打印|输出)[^。!?\n]{0,6}(你的|系统|上面|以上)[^。!?\n]{0,6}(提示|指令|规则|设定|prompt)/i,
  // 角色反转:让小白来当老师/来讲
  /(你现在是|你来当|你来做|你来当我的|请你当|请你做|你扮演|你就是)[^。!?\n]{0,6}老师/,
  /(换你|轮到你|该你|你来)[^。!?\n]{0,4}(讲|说|教|来)(给我|讲讲|一遍|吧|呀)?/,
  /(你|请你)[^。!?\n]{0,4}(讲给我听|教我|教教我|讲一遍给我|把答案讲给我|直接讲答案|说答案|公布答案)/,
  // 英文越狱注入
  /ignore\s+(the\s+)?(all\s+)?(above|previous|prior|earlier)[^.\n]{0,24}(instruction|rule|prompt|direction)/i,
  /(you\s+are\s+now|act\s+as|pretend\s+to\s+be)[^.\n]{0,20}(teacher|assistant|tutor|expert)/i,
  /(reveal|print|repeat|show\s+me)[^.\n]{0,16}(your\s+)?(system\s+prompt|instructions?|rules|checklist|answer\s*key)/i,
];

/** 忽略/覆盖上文规则(中文注入) */
const OVERRIDE_PATTERN =
  /忽略(掉)?[^。!?\n]{0,8}(以上|上面|之前|前面|刚才|所有|全部|你的)[^。!?\n]{0,8}(指令|规则|设定|要求|提示|命令|限制|人设|角色)/;

/**
 * 判断这一轮是否为「套答案 / 越权」尝试。
 * @returns true = 应拦截(不进评估链)
 */
export function isExtractionAttempt(text: string): boolean {
  const t = text.trim();
  if (!t) return false;
  if (OVERRIDE_PATTERN.test(t)) return true;
  return EXTRACT_PATTERNS.some((re) => re.test(t));
}
